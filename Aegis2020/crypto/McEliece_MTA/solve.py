def encrypt(m):
    global G
    cipher_bit = [0]*56
    for i in range(56):
        for j in range(16):
            cipher_bit[i] = cipher_bit[i] ^ (m[j] * G[j][i])
    return cipher_bit

def cipher_to_binary(cipher):
    bit = ""
    for c in range(0, len(cipher), 2):
        tmp = cipher[c] + cipher[c+1]
        bit += "{:08b}".format(int(tmp, 16))
    return list(map(int, list(bit)))

def error_bit(plaintext, cipher):
    return list(map(lambda x,y : x^y, plaintext, cipher)).count(1)

def char_to_bin(char):
    return chr(int(''.join(list(map(str, char)))[:8], 2)) + chr(int(''.join(list(map(str, char)))[8:], 2))

